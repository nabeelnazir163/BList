import UIKit

extension UIFont {
   
    class func cabin_Bold(size:CGFloat) -> UIFont {
        return UIFont.init(name: "Cabin-Bold", size: size)!
    }
    class func cabin_SemiBold(size:CGFloat) -> UIFont {
        return UIFont.init(name: "Cabin-SemiBold", size: size)!
    }
    class func cabin_Regular(size:CGFloat) -> UIFont {
        return UIFont.init(name: "Cabin-Regular", size: size)!
    }
    class func cabin_Medium(size:CGFloat) -> UIFont {
        return UIFont.init(name: "Cabin-Medium", size: size)!
    }
    class func cerebriSans_SemiBold(size:CGFloat) -> UIFont {
        return UIFont.init(name: "CerebriSans-SemiBold", size: size)!
    }
    class func cerebriSans_Book(size:CGFloat) -> UIFont {
        return UIFont.init(name: "CerebriSans-Book", size: size)!
    }
    
}
